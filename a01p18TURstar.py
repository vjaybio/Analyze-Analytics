import turtle             # Imports Turtle module and its functionalities
turtle.showturtle()            # shows Turtle cursor
turtle.right(72)            # Turns the cursor by an angle of 72 degrees to right
turtle.forward(125)            # draws the cursor forward by 125 pixels
turtle.right(144)
turtle.forward(125)
turtle.right(144)
turtle.forward(125)
turtle.right(144)
turtle.forward(125)
turtle.right(144)
turtle.forward(125)
turtle.done()
